package com.jay.bean.weather;

/**
 * 天文数值
 * 
 * @author Jay
 * 
 */
public class Astro {

	/**
	 * 日出时间
	 */
	private String sr;
	/**
	 * 日落时间
	 */
	private String ss;

	public void setSr(String sr) {
		this.sr = sr;
	}

	public String getSr() {
		return sr;
	}

	public void setSs(String ss) {
		this.ss = ss;
	}

	public String getSs() {
		return ss;
	}

}